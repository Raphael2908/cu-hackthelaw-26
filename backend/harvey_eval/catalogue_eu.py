"""Catalogue the EU-jurisdiction Harvey Labs tasks.

Why: our shipped citation model is CELEX/EUR-Lex-shaped, so EU-law tasks (esp. GDPR)
are the slice of Harvey where our worker's citations + the `citation_support` /
`precedent_deviation` signals can fire faithfully (see system-design/harvey-eval.md).
This scans tasks/ for EU vs US jurisdiction markers and writes a catalogue future
runs can pick the EU/GDPR lane from.

    uv run python -m harvey_eval.catalogue_eu     # regenerates EU_TASKS.md + eu_tasks.json

CAVEAT: detection is a text proxy over each task.json (instructions + criteria), not the
underlying documents, so it UNDER-counts (a task can be EU in its docs without naming a
statute in the rubric) and markers are heuristic. Treat counts as a band, not exact.
"""

from __future__ import annotations

import json
import re
from pathlib import Path

HARVEY_ROOT = Path(__file__).resolve().parents[2] / "harvey-labs"
TASKS_ROOT = HARVEY_ROOT / "tasks"
HERE = Path(__file__).resolve().parent

# Curated jurisdiction markers. EU patterns map to a normalized label for the catalogue.
EU_MARKERS = {
    "GDPR": r"\bGDPR\b",
    "EU-SCC": r"\bStandard Contractual Clauses\b|\bSCCs?\b",
    "Schrems": r"\bSchrems\b",
    "TFEU/Art101-102": r"\bTFEU\b|\bArticle 10[12]\b",
    "CJEU": r"\bCJEU\b|Court of Justice of the European Union",
    "EUR-Lex/CELEX": r"\bEUR-?Lex\b|\bCELEX\b",
    "EU-Regulation": r"Regulation \(EU\)",
    "EU-Directive": r"\bDirective \d{2,4}/\d+|\bDirective \(EU\)",
    "EU-AI-Act": r"\bEU AI Act\b|\bAI Act\b",
    "MiFID/MAR/DORA": r"\bMiFID\b|\bMAR\b|\bDORA\b|\bMiCA\b",
    "EU-generic": r"\bEuropean Union\b|\bEuropean Commission\b|\bEU member state\b|\bEEA\b",
    "UK-GDPR/ICO": r"\bUK GDPR\b|\bICO\b",
}
US_MARKER = re.compile(
    r"\b(U\.S\.C|Sherman Act|FTC Act|Clayton|HSR|\bSEC\b|Delaware|Securities Exchange Act"
    r"|Internal Revenue Code|Dodd-Frank|NLRA|Title VII|Bankruptcy Code|Securities Act of 1933)\b"
)
READABLE = {".docx", ".pdf", ".txt", ".md", ".eml", ""}


def scan() -> list[dict]:
    rows = []
    for tj in TASKS_ROOT.rglob("task.json"):
        try:
            c = json.loads(tj.read_text(encoding="utf-8"))
        except Exception:
            continue
        text = c.get("instructions", "") + " " + " ".join(
            cr.get("match_criteria", "") + " " + cr.get("title", "") for cr in c.get("criteria", [])
        )
        matched = sorted(lbl for lbl, pat in EU_MARKERS.items() if re.search(pat, text, re.I))
        eu = sum(len(re.findall(pat, text, re.I)) for pat in EU_MARKERS.values())
        us = len(US_MARKER.findall(text))
        if eu == 0:
            continue
        tid = "/".join(tj.parent.relative_to(TASKS_ROOT).parts)
        docs = [p for p in (tj.parent / "documents").rglob("*") if p.is_file()] \
            if (tj.parent / "documents").exists() else []
        n_read = sum(1 for p in docs if p.suffix.lower() in READABLE)
        rows.append({
            "id": tid,
            "area": tj.parent.relative_to(TASKS_ROOT).parts[0],
            "title": c.get("title", ""),
            "work_type": c.get("work_type", ""),
            "n_criteria": len(c.get("criteria", [])),
            "eu": eu,
            "us": us,
            "eu_markers": matched,
            "n_docs": len(docs),
            "readable_docs": n_read,
            "eu_dominant": eu >= 2 and eu > us,
            "eu_pure": us == 0,
        })
    rows.sort(key=lambda r: (r["area"], -r["eu"], r["id"]))
    return rows


def write_md(rows: list[dict]) -> None:
    by_area: dict[str, list[dict]] = {}
    for r in rows:
        by_area.setdefault(r["area"], []).append(r)

    lines = [
        "# Harvey Labs — EU-jurisdiction task catalogue",
        "",
        "> Auto-generated by `backend/harvey_eval/catalogue_eu.py`. Regenerate after pulling new",
        "> tasks. **Caveat:** text-proxy over each `task.json` (instructions + criteria), so it",
        "> under-counts and markers are heuristic — treat counts as a band.",
        "",
        "**Why this exists:** our shipped citation model is CELEX/EUR-Lex-shaped. These EU-law",
        "tasks (esp. GDPR/data-privacy) are the slice of Harvey where our worker's citations and",
        "the `citation_support` / `precedent_deviation` signals can fire faithfully. To run the",
        "EU/GDPR lane: pick `eu_pure` issue-spotting tasks below, seed the GDPR + SCC instruments",
        "as CELEX-tagged corpus docs, then run the pipeline (see system-design/harvey-eval.md).",
        "",
        f"**Totals:** {len(rows)} tasks with EU markers · "
        f"{sum(1 for r in rows if r['eu_dominant'])} EU-dominant · "
        f"{sum(1 for r in rows if r['eu_pure'])} EU-pure (no US marker).",
        "",
        "## By practice area",
        "",
        "| Area | EU tasks | EU-pure |",
        "|---|---:|---:|",
    ]
    for area in sorted(by_area, key=lambda a: -len(by_area[a])):
        rs = by_area[area]
        lines.append(f"| {area} | {len(rs)} | {sum(1 for r in rs if r['eu_pure'])} |")

    lines += ["", "## Full catalogue", ""]
    for area in sorted(by_area, key=lambda a: -len(by_area[a])):
        lines.append(f"### {area}")
        lines.append("")
        lines.append("| task | type | crit | eu | us | docs(read) | markers |")
        lines.append("|---|---|---:|---:|---:|---|---|")
        for r in by_area[area]:
            star = "★" if r["eu_pure"] else ("•" if r["eu_dominant"] else "")
            short = r["id"][len(area) + 1:]
            lines.append(
                f"| {star}{short} | {r['work_type']} | {r['n_criteria']} | {r['eu']} | {r['us']} "
                f"| {r['n_docs']}({r['readable_docs']}) | {', '.join(r['eu_markers'])} |"
            )
        lines.append("")
    lines += ["★ = EU-pure (no US marker) · • = EU-dominant", ""]
    (HERE / "EU_TASKS.md").write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    rows = scan()
    (HERE / "eu_tasks.json").write_text(json.dumps(rows, indent=2), encoding="utf-8")
    write_md(rows)
    print(f"Catalogued {len(rows)} EU-marker tasks "
          f"({sum(1 for r in rows if r['eu_pure'])} EU-pure, "
          f"{sum(1 for r in rows if r['eu_dominant'])} EU-dominant).")
    print(f"  -> {HERE / 'eu_tasks.json'}")
    print(f"  -> {HERE / 'EU_TASKS.md'}")


if __name__ == "__main__":
    main()
